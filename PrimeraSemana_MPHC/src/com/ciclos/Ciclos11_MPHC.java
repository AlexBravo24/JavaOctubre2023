package com.ciclos;

import java.util.Scanner;

public class Ciclos11_MPHC {

	public static void main(String[] args) {
		Scanner entrada = new Scanner(System.in);

		System.out.println("Ingrese slto del cuadrado");
		int alto = entrada.nextInt();
		
		System.out.println("Ingrese slto del ancho");
		int ancho = entrada.nextInt();
		
		for (int i = 1; i <=alto; i++) {
			
			for (int u = 1; u <=ancho; u++) {
				
				
				{
					System.out.print("* ");
				}
			
				
			}
	
			System.out.println();
			
		}
		

	}

}
